#include"bits/stdc++.h"
using namespace std;

int main()
{
    int ch;
    cin>>ch;

    switch(ch)
    {
        case 1:
            cout<<"Good"<<endl;
            break;
        case 2:
            cout<<"Better"<<endl;
            break;
        case 3:
            cout<<"Best"<<endl;
            break; 
        default:
            cout<<"Invalid "<<endl;          
    }
}